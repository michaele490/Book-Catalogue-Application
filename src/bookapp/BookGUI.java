    public Book getMostExpensive(){
        Book mostExpensive = books.get(0);
        for(Book b:books){
            if(mostExpensive.getCost() < b.getCost()){
                mostExpensive = b;
            }
        }
        return mostExpensive;
    }
    
    private void mostExpensiveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mostExpensiveBtnActionPerformed
        // TODO add your handling code here:
        Book mostExpensiveBook = getMostExpensive(); 
        if(mostExpensiveBook != null){
            JOptionPane.showMessageDialog(null, "The most expensive book\n" + mostExpensiveBook.getDetails());
        }
        else{
            JOptionPane.showMessageDialog(null, "Please enter details for the cost of a book");
        }
    }//GEN-LAST:event_mostExpensiveBtnActionPerformed

