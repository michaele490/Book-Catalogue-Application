    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
        if(bookRB.isSelected()){
            Book b = new Book();
            b.setIdNum(idTF.getText());
            b.setTitle(titleTF.getText());
            b.setAuthor(authorTF.getText());
            b.setCost(Double.parseDouble(costTF.getText()));
            
            books.add(b);
            bookDetailsTA.append("New hard copy added to catalogue\n");
        }
        else if(eBookRB.isSelected()){
            EBook e = new EBook();
            e.setIdNum(idTF.getText());
            e.setTitle(titleTF.getText());
            e.setAuthor(authorTF.getText());
            e.setCost(Double.parseDouble(costTF.getText()));
            e.setFileFormat(fileFormatTF.getText());
            
            books.add(e);
            bookDetailsTA.append("New E-Book added to catalogue\n");
        }
        else if(audioBookRB.isSelected()){
            AudioBook a = new AudioBook();
            a.setIdNum(idTF.getText());
            a.setTitle(titleTF.getText());
            a.setAuthor(authorTF.getText());
            a.setCost(Double.parseDouble(costTF.getText()));
            a.setNarrator(narratorTF.getText());
            
            books.add(a);
            bookDetailsTA.append("New audio book added to catalogue\n");
        }
    }//GEN-LAST:event_addBtnActionPerformed

