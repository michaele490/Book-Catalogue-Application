    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        File f;
        FileOutputStream fStream;
        ObjectOutputStream oStream;
        
        try{
            f = new File("Book Catalogue.dat");
            fStream = new FileOutputStream(f);
            oStream = new ObjectOutputStream(fStream);
            
            oStream.writeObject(books);
            oStream.close();
        }
        catch(IOException e){
            JOptionPane.showMessageDialog(null, e);
        }
        
        bookDetailsTA.append("Books saved to catalogue");
    }//GEN-LAST:event_saveBtnActionPerformed

    private void loadBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loadBtnActionPerformed
        // TODO add your handling code here:
        File f;
        FileInputStream fStream;
        ObjectInputStream oStream;
        
        try{
            f = new File("Book Catalogue.dat");
            fStream = new FileInputStream(f);
            oStream = new ObjectInputStream(fStream);
            
            books = (ArrayList<Book>)oStream.readObject();
            oStream.close();
        }
        catch(IOException e){
            JOptionPane.showMessageDialog(null, e);
        }
        catch(ClassNotFoundException ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_loadBtnActionPerformed

    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        String searchId;
        searchId = JOptionPane.showInputDialog(null, "Enter the ID of the book you want to search for");
        for(Book b:books){
            if(b.getIdNum().equals(searchId)){
                JOptionPane.showMessageDialog(null, b.getDetails());
            }
        }
    }//GEN-LAST:event_searchBtnActionPerformed
    
